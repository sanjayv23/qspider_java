package may15;

public class Bag {
	private  String color;
	private int price;
	
	//setter
	public void setColor(String color) {
		this.color=color;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	
	//getter
	
	public String getColor() {
		return this.color;
	}
	public int getPrice() {
		return this.price;
	}
}
